export type Lang = 'ko' | 'ja' | 'en';

const LS_KEY = 'kiikii_lang';

export function getLang(): Lang {
  return (localStorage.getItem(LS_KEY) as Lang) ?? 'ko';
}

export function setLang(lang: Lang): void {
  localStorage.setItem(LS_KEY, lang);
}

